package march.twozero.twentieth;

public class ArrayUtility {
	public static double[] intToDouble(int[] source) { // int배열을 double배열로 변환
		double[] arr1 = new double[source.length];
		
		for (int i = 0; i < source.length; i++)
			arr1[i] = (double)Math.ceil(source[i]);
		
		for(int i = 0; i < source.length; i++)
			System.out.print(source[i] + ", ");
		
		return arr1;	
	}

	public static int[] doubleToInt(double[] source) { // double배열을 int배열로 변환
		int[] arr2 = new int[source.length];
		
		for (int i = 0; i < source.length; i++)
			arr2[i] = (int)Math.ceil(source[i]);
		
		for(int i = 0; i < source.length; i++)
			System.out.print(source[i] + ", ");
		
		return arr2;
	}
}
